// sting methods

// lenght ;-used to find the length of the string

let str = " hello world "
console.log(str.length);

// charAt :- used to find the index value of the character in the string

console.log(str.charAt(7))

// trim :- used to remove the unwanted spaces from the begining and from the end
 
console.log(str.trim());

// concat :- used to concatinate 2 strings
let str2 = "aliens"
console.log(str.concat(str2));

// substring  :- used to find the substring of the given strings

console.log(str.substring(5,8));

// toupperCase :- used to convert the string to the uppercase 

console.log(str.toUpperCase());

// tolowercase :- used to convert the string to the lowercase

console.log(str.toLowerCase());

// slice :- used to slice the string using the specified index

console.log(str.slice(4,8));