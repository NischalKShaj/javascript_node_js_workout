// string methods

// to see the value of the string at the specified position

let str = "hello world"
let nstr1 = str.charAt(4)
console.log(nstr1)

// to make all the value of the string in the upper case

let nstr2 = str.toUpperCase()
console.log(nstr2);

// to make all the value of the string to the lower case

let nstr3 = str.toLowerCase()
console.log(nstr3);

// to concatenate a string

let str2 = "idiots"
let nstr4 = str.concat(str2)
console.log(nstr4);

// to find the length of the string

let len = str.length
console.log(len)

// replacing the value with another string

let nstr5 = str.replace("world","universe")
console.log(nstr5);